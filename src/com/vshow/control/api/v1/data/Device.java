package com.vshow.control.api.v1.data;

import java.util.List;

import com.vshow.control.data.Item;

public class Device {
	
	private int id;
	private String name;
	private int state;
	private String mark;
	private String screenshot; //截屏 bast64串
	private List<Item> items;  //节目播放列表
	private Integer playState; //1播放  2暂停  3停止
	
	private ClientState clientState;

	
	
	public ClientState getClientState() {
		return clientState;
	}
	public void setClientState(ClientState clientState) {
		this.clientState = clientState;
	}
	public Integer getPlayState() {
		return playState;
	}
	public void setPlayState(Integer playState) {
		this.playState = playState;
	}
	public String getScreenshot() {
		return screenshot;
	}
	public void setScreenshot(String screenshot) {
		this.screenshot = screenshot;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<Item> getItems() {
		return items;
	}
	public void setItems(List<Item> items) {
		this.items = items;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public String getMark() {
		return mark;
	}
	public void setMark(String mark) {
		this.mark = mark;
	}

}
