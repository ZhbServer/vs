package com.vshow.control.protocol;

import java.util.ArrayList;
import java.util.List;

public class ProtocolPlay   {
	
	private int day;      // 轮播节目周期
	private String sdate; // 播放开始时间    插播格式  yyyy-MM-dd HH:mm:ss /轮播格式 HH:mm:ss
	private String edate; // 播放结束时间    插播格式  yyyy-MM-dd HH:mm:ss /轮播格式 HH:mm:ss
	private List<String> playList = new ArrayList<String>(); // 播放列表
	
	private String account;//用户登陆名 
	
	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public String getSdate() {
		return sdate;
	}

	public void setSdate(String sdate) {
		this.sdate = sdate;
	}

	public String getEdate() {
		return edate;
	}

	public void setEdate(String edate) {
		this.edate = edate;
	}

	public List<String> getPlayList() {
		return playList;
	}

	public void setPlayList(List<String> playList) {
		this.playList = playList;
	}

	
}
