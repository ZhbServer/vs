package com.vshow.control.protocol;

public class ProtocolDownResource {
	
	private int downType; // 下载类型 0本机 1乐视云
	private String fileName; // 文件名
	private String downUrl; // 下载地址
	private String videoUnique; // 乐视云下载 videoUnique
	private String fileMd5;     //文件MD5 

	


	public String getFileMd5() {
		return fileMd5;
	}

	public void setFileMd5(String fileMd5) {
		this.fileMd5 = fileMd5;
	}

	public int getDownType() {
		return downType;
	}

	public void setDownType(int downType) {
		this.downType = downType;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getDownUrl() {
		return downUrl;
	}

	public void setDownUrl(String downUrl) {
		this.downUrl = downUrl;
	}

	public String getVideoUnique() {
		return videoUnique;
	}

	public void setVideoUnique(String videoUnique) {
		this.videoUnique = videoUnique;
	}

	
}
