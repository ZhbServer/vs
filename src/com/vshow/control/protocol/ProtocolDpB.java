package com.vshow.control.protocol;

import java.util.ArrayList;
import java.util.List;

public class ProtocolDpB   {
	
	
	private int day; // 垫片节目周期
	private int jd; // 节点数量
	private List<ProtocolDp> protocolDp;  //节点集合
	
	public List<ProtocolDp> getProtocolDp() {
		return protocolDp;
	}

	public void setProtocolDp(List<ProtocolDp> protocolDp) {
		this.protocolDp = protocolDp;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public int getJd() {
		return jd;
	}

	public void setJd(int jd) {
		this.jd = jd;
	}
	

	
}
