package com.vshow.control.protocol;

import java.util.ArrayList;
import java.util.List;

public class ProtocolPlayAccurate   {
	 
	private int sex;  //性别    0男女  1男 2女
    private String generation;  //年龄范围   格式(0,100)
	private List<String> playList = new ArrayList<String>(); // 播放列表
	private int fbmode;  //0替换 1追加
	
		
	
	

	
	public int getFbmode() {
		return fbmode;
	}

	public void setFbmode(int fbmode) {
		this.fbmode = fbmode;
	}

	public int getSex() {
		return sex;
	}

	public void setSex(int sex) {
		this.sex = sex;
	}

	public String getGeneration() {
		return generation;
	}

	public void setGeneration(String generation) {
		this.generation = generation;
	}

	public List<String> getPlayList() {
		return playList;
	}

	public void setPlayList(List<String> playList) {
		this.playList = playList;
	}

	
}
