package com.vshow.control.data;

public class Fbl extends PublicData {
	private int id;
	private String name;
	private int tx;
	private int ty;
	private int ycCount;
	
	
	

	public int getYcCount() {
		return ycCount;
	}

	public void setYcCount(int ycCount) {
		this.ycCount = ycCount;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getTx() {
		return tx;
	}

	public void setTx(int tx) {
		this.tx = tx;
	}

	public int getTy() {
		return ty;
	}

	public void setTy(int ty) {
		this.ty = ty;
	}
}
