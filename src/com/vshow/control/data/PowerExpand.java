package com.vshow.control.data;

public class PowerExpand {
	
    private int id;
    private String expandContent;
	private int powerId;
	
	private String powerName;  //expandContent 权限名
	private int powerState;    //状态 0 无权限 1有	权限

	public String getPowerName() {
		return powerName;
	}

	public void setPowerName(String powerName) {
		this.powerName = powerName;
	}

	public int getPowerState() {
		return powerState;
	}

	public void setPowerState(int powerState) {
		this.powerState = powerState;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getExpandContent() {
		return expandContent;
	}

	public void setExpandContent(String expandContent) {
		this.expandContent = expandContent;
	}

	public int getPowerId() {
		return powerId;
	}

	public void setPowerId(int powerId) {
		this.powerId = powerId;
	}
}
