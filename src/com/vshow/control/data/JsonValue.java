package com.vshow.control.data;

public class JsonValue {
	private String code;
	private String mark;  
	private int state;  //1成功 2缺纸 3卡纸 4切纸机未开启 5终端机器超时未反馈信息 6终端未录入/未认证
	
	
	
	public String getMark() {
		return mark;
	}

	public void setMark(String mark) {
		this.mark = mark;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
