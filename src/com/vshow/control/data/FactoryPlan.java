package com.vshow.control.data;

public class FactoryPlan extends PublicData{

	private int id;			//id
	private String name;	//计划名
	private int itemC;		//明细数量

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getItemC() {
		return itemC;
	}

	public void setItemC(int itemC) {
		this.itemC = itemC;
	}

}
