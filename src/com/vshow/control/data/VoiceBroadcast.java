package com.vshow.control.data;

public class VoiceBroadcast extends PublicData {
	private int id;
	private int numberstr;           //语音播报编号 
	private int uid;                    //创建用户
	private String resourcefilename;    //语音播报文件名(filename)
	private String resourcename;        //语音播报文件名
	private String contents;            //语音播报内容
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public int getNumberstr() {
		return numberstr;
	}
	public void setNumberstr(int numberstr) {
		this.numberstr = numberstr;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getResourcefilename() {
		return resourcefilename;
	}
	public void setResourcefilename(String resourcefilename) {
		this.resourcefilename = resourcefilename;
	}
	public String getResourcename() {
		return resourcename;
	}
	public void setResourcename(String resourcename) {
		this.resourcename = resourcename;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	

}
