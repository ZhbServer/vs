package com.vshow.control.data.tq;

import java.util.List;

public class TqData {
	
	private int error=0;
	private String status="success";
	private String date="";
	private List<TqCity> results;
	
	
	public int getError() {
		return error;
	}
	public void setError(int error) {
		this.error = error;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public List<TqCity> getResults() {
		return results;
	}
	public void setResults(List<TqCity> results) {
		this.results = results;
	}
	
	

}
