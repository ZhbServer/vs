package com.vshow.control.data;

/***
 * 终端预警节目
 * 
 * @author Administrator
 * 
 */
public class ClientWarningItem extends PublicData {
	
	private int id; // id
	
	private String mark; //标识码
	
	private int  type ;  //插播 17
	
	private String msg;  
	
	private String sdate;  //节目开始时间
	
	private String edate;  //节目结束时间
	
	private String itemfiles; //节目文件多个用,分割
	
	private int uid;
	
	private String clientname;  //终端名
	
	private String itemNames;   //节目名(多个用','分割) 
	
	private int state;          //0未接受 2 操作成功
	
	private String sendkey;  
	
	private int dayDiff;  //预警节目天数
	
	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public String getSendkey() {
		return sendkey;
	}

	public void setSendkey(String sendkey) {
		this.sendkey = sendkey;
	}

	public int getDayDiff() {
		return dayDiff;
	}

	public void setDayDiff(int dayDiff) {
		this.dayDiff = dayDiff;
	}

	public String getItemNames() {
		return itemNames;
	}

	public void setItemNames(String itemNames) {
		this.itemNames = itemNames;
	}

	public String getClientname() {
		return clientname;
	}

	public void setClientname(String clientname) {
		this.clientname = clientname;
	}

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMark() {
		return mark;
	}

	public void setMark(String mark) {
		this.mark = mark;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getSdate() {
		return sdate;
	}

	public void setSdate(String sdate) {
		this.sdate = sdate;
	}

	public String getEdate() {
		return edate;
	}

	public void setEdate(String edate) {
		this.edate = edate;
	}

	public String getItemfiles() {
		return itemfiles;
	}

	public void setItemfiles(String itemfiles) {
		this.itemfiles = itemfiles;
	}
	
	
	
	

}
